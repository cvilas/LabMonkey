#include "Motor.h"
#include "AppBoard.h"
#include <sstream>

//==============================================================================
Motor::Motor(int node)
//==============================================================================
    : _node(node), _maxVel(0)
{
    enable();
}

//------------------------------------------------------------------------------
void Motor::setMaxVelocity(int v)
//------------------------------------------------------------------------------
{
    _maxVel = v;
}

//------------------------------------------------------------------------------
bool Motor::enable()
//------------------------------------------------------------------------------
{
    if( _node == 0 )
    {
        AppBoard::robotWrite("EN");
    }
    else
    {
        std::ostringstream str;
        str << _node << "EN";
        AppBoard::robotWrite(str.str());
    }
}
